package net.manmon.pkg;

public class PkgInfo {
    private Short arch;
    private Long nameId;
    private Long verId;

    public Short getArch() {
        return arch;
    }

    public void setArch(Short arch) {
        this.arch = arch;
    }

    public Long getNameId() {
        return nameId;
    }

    public void setNameId(Long nameId) {
        this.nameId = nameId;
    }

    public Long getVerId() {
        return verId;
    }

    public void setVerId(Long verId) {
        this.verId = verId;
    }
}
